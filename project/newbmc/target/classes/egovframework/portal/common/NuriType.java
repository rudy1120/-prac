package egovframework.portal.common;

public enum NuriType {

	NO_USE("0", "사용 안 함"),
	TYPE1("1", "공공누리 제1유형: 출처표시(상업적 이용 및 변경 가능)"),
	TYPE2("2", "공공누리 제2유형: 출처표시 + 상업적 이용 금지"),
	TYPE3("3", "공공누리 제3유형: 출처표시 + 변경 금지"),
	TYPE4("4", "공공누리 제4유형: 출처표시 + 상업적 이용 및 변경 금지"),
	TYPE5("5", "자유이용 불가(저작권법 제24조의2 제1항 제1호 ~ 4호 중 어느 하나에 해당됨)"),
	;

	private String code;
	private String desc;

	private NuriType(String code, String desc) {
		this.code = code;
		this.desc = desc;
	}

	public String getCode() {
		return code;
	}

	public String getDesc() {
		return desc;
	}

}
