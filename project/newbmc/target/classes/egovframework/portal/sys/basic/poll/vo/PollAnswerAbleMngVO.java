public class PollAnswerAbleMngVO {

	private int idx = 0;
	/** 질문 번호 */
	private String questionNum = "";
	/** 응답 조건 (JSONArray 데이터) */
	private String target = "";
	/** 등록 날짜 */
	private Date createDate = null;
	/** 질문 IDX */
	private int questionIdx = 0;
	/** 설문 IDX */
	private int pollIdx = 0;

	public PollAnswerAbleMngVO() {
		super();
	}

	public PollAnswerAbleMngVO(int pollIdx) {
		super();
		this.pollIdx = pollIdx;
	}

	public int getIdx() {
		return idx;
	}

	public void setIdx(int idx) {
		this.idx = idx;
	}

	public String getQuestionNum() {
		return questionNum;
	}

	public void setQuestionNum(String questionNum) {
		this.questionNum = questionNum;
	}

	public String getTarget() {
		return target;
	}

	public void setTarget(String target) {
		this.target = target;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public int getQuestionIdx() {
		return questionIdx;
	}

	public void setQuestionIdx(int questionIdx) {
		this.questionIdx = questionIdx;
	}

	public int getPollIdx() {
		return pollIdx;
	}

	public void setPollIdx(int pollIdx) {
		this.pollIdx = pollIdx;
	}

}