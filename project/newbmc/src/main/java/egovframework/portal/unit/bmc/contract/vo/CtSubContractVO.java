package egovframework.portal.unit.bmc.contract.vo;

public class CtSubContractVO {

	
	/* 계약번호 */
	private String cntrNo;
	
	/* 하도급연번 */
	private String subcontractNo;
	
	/* 공종 */
	private String workKind;
	
	/* 업체명 */
	private String subcompNm;
	
	/* 계약일자 */
	private String cntrYrmd;
	
	/* 하도급액 */
	private double hcntrAmt;
	
	/* 하도급율 */
	private String contractRate;
	
	/* 최초입력일 */
	private String frstInsYrmd;
	
	/* 최초입력자 */
	private String frstInsEno;

	public String getCntrNo() {
		return cntrNo;
	}

	public void setCntrNo(String cntrNo) {
		this.cntrNo = cntrNo;
	}

	public String getSubcontractNo() {
		return subcontractNo;
	}

	public void setSubcontractNo(String subcontractNo) {
		this.subcontractNo = subcontractNo;
	}

	public String getWorkKind() {
		return workKind;
	}

	public void setWorkKind(String workKind) {
		this.workKind = workKind;
	}

	public String getSubcompNm() {
		return subcompNm;
	}

	public void setSubcompNm(String subcompNm) {
		this.subcompNm = subcompNm;
	}

	public String getCntrYrmd() {
		return cntrYrmd;
	}

	public void setCntrYrmd(String cntrYrmd) {
		this.cntrYrmd = cntrYrmd;
	}

	public double getHcntrAmt() {
		return hcntrAmt;
	}

	public void setHcntrAmt(double hcntrAmt) {
		this.hcntrAmt = hcntrAmt;
	}

	public String getContractRate() {
		return contractRate;
	}

	public void setContractRate(String contractRate) {
		this.contractRate = contractRate;
	}

	public String getFrstInsYrmd() {
		return frstInsYrmd;
	}

	public void setFrstInsYrmd(String frstInsYrmd) {
		this.frstInsYrmd = frstInsYrmd;
	}

	public String getFrstInsEno() {
		return frstInsEno;
	}

	public void setFrstInsEno(String frstInsEno) {
		this.frstInsEno = frstInsEno;
	}

	@Override
	public String toString() {
		return "CtSubContractVO [cntrNo=" + cntrNo + ", subcontractNo=" + subcontractNo + ", workKind=" + workKind
				+ ", subcompNm=" + subcompNm + ", cntrYrmd=" + cntrYrmd + ", hcntrAmt=" + hcntrAmt + ", contractRate="
				+ contractRate + ", frstInsYrmd=" + frstInsYrmd + ", frstInsEno=" + frstInsEno + "]";
	}


	
}
