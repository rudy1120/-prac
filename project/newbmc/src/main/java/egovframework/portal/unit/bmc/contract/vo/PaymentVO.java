package egovframework.portal.unit.bmc.contract.vo;

import egovframework.portal.common.vo.CommonVO;

public class PaymentVO extends CommonVO {
	
	/* 계약번호 */
	private String cntrNo;
	
	/* 지급연번 */
	private String payNo;
	
	/* 지급일 */
	private String payYrmd;
	
	/* 지급종류 */
	private String payGb;
	
	/* 지급액 */
	private long payAmt;
	
	/* 최초입력일 */
	private String frstInsYrmd;
	
	/* 최초입력자 */
	private String frstInsEno;
	
	/* 계약명 */
	private String cntrNm;
	
	/* 업체명 */
	private String compNm;
	
	private String firstDay;
	
	private String lastDay;
	
	private int condition;
	
	public int getCondition() {
		return condition;
	}

	public void setCondition(int condition) {
		this.condition = condition;
	}

	public String getFirstDay() {
		return firstDay;
	}

	public void setFirstDay(String firstDay) {
		this.firstDay = firstDay;
	}

	public String getLastDay() {
		return lastDay;
	}

	public void setLastDay(String lastDay) {
		this.lastDay = lastDay;
	}

	public String getCntrNo() {
		return cntrNo;
	}

	public void setCntrNo(String cntrNo) {
		this.cntrNo = cntrNo;
	}

	public String getPayNo() {
		return payNo;
	}

	public void setPayNo(String payNo) {
		this.payNo = payNo;
	}

	public String getPayYrmd() {
		return payYrmd;
	}

	public void setPayYrmd(String payYrmd) {
		this.payYrmd = payYrmd;
	}

	public String getPayGb() {
		return payGb;
	}

	public void setPayGb(String payGb) {
		this.payGb = payGb;
	}

	public long getPayAmt() {
		return payAmt;
	}

	public void setPayAmt(long payAmt) {
		this.payAmt = payAmt;
	}

	public String getFrstInsYrmd() {
		return frstInsYrmd;
	}

	public void setFrstInsYrmd(String frstInsYrmd) {
		this.frstInsYrmd = frstInsYrmd;
	}

	public String getFrstInsEno() {
		return frstInsEno;
	}

	public void setFrstInsEno(String frstInsEno) {
		this.frstInsEno = frstInsEno;
	}

	public String getCntrNm() {
		return cntrNm;
	}

	public void setCntrNm(String cntrNm) {
		this.cntrNm = cntrNm;
	}

	public String getCompNm() {
		return compNm;
	}

	public void setCompNm(String compNm) {
		this.compNm = compNm;
	}

	@Override
	public String toString() {
		return "PaymentVO [cntrNo=" + cntrNo + ", payNo=" + payNo + ", payYrmd=" + payYrmd + ", payGb=" + payGb
				+ ", payAmt=" + payAmt + ", frstInsYrmd=" + frstInsYrmd + ", frstInsEno=" + frstInsEno + ", cntrNm="
				+ cntrNm + ", compNm=" + compNm + ", firstDay=" + firstDay + ", lastDay=" + lastDay + "]";
	}

	
}
