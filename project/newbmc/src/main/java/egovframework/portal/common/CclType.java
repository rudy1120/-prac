package egovframework.portal.common;

public enum CclType {

	NO_USE("0", "사용 안 함"),
	TYPE1("1", "저작자 표시 4.0 국제 라이선스"),
	TYPE2("2", "저작자 표시 - 변경 금지 4.0 국제 라이선스"),
	TYPE3("3", "저작자 표시 - 동일 조건 변경 허락 4.0 국제 라이선스"),
	TYPE4("4", "저작자 표시 - 비영리 4.0 국제 라이선스"),
	TYPE5("5", "저작자 표시 - 비영리 - 변경금지 4.0 국제 라이선스"),
	TYPE6("6", "저작자 표시 - 비영리 - 동일 조건 변경 허락 4.0 국제 라이선스"),
	;

	private String code;
	private String desc;

	private CclType(String code, String desc) {
		this.code = code;
		this.desc = desc;
	}

	public String getCode() {
		return code;
	}

	public String getDesc() {
		return desc;
	}

}
