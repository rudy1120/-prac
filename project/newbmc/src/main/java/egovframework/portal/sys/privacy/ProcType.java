public enum ProcType {

	INSERT("i", "등록"),
	UPDATE("u", "수정"),
	DELETE("d", "삭제"),
	;

	private String code;
	private String name;

	private ProcType(String code, String name) {
		this.code = code;
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public String getName() {
		return name;
	}

}
