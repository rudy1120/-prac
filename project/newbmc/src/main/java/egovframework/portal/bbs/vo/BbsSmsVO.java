package egovframework.portal.bbs.vo;

public class BbsSmsVO {

	private String sIdx = "";
	private String ptIdx = "";
	private String sAdmin = "";
	private String sHp = "";
	private String sDeptNm = "";
	private String ptTitle = "";

	public String getsIdx() {
		return sIdx;
	}

	public void setsIdx(String sIdx) {
		this.sIdx = sIdx;
	}

	public String getPtIdx() {
		return ptIdx;
	}

	public void setPtIdx(String ptIdx) {
		this.ptIdx = ptIdx;
	}

	public String getsAdmin() {
		return sAdmin;
	}

	public void setsAdmin(String sAdmin) {
		this.sAdmin = sAdmin;
	}

	public String getsHp() {
		return sHp;
	}

	public void setsHp(String sHp) {
		this.sHp = sHp;
	}

	public String getsDeptNm() {
		return sDeptNm;
	}

	public void setsDeptNm(String sDeptNm) {
		this.sDeptNm = sDeptNm;
	}

	public String getPtTitle() {
		return ptTitle;
	}

	public void setPtTitle(String ptTitle) {
		this.ptTitle = ptTitle;
	}

}
