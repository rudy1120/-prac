public class SiteAccessVO extends CommonVO implements Serializable {

	private static final long serialVersionUID = 1L;

	private int seq = 0;
	private String ip = "";
	private String useYn = "";
	private Date regDt = null;
	private Date modDt = null;
	private String title = "";
	private String type = "";
	private String ipBandA = "";
	private String ipBandB = "";

	private String allowDateUseYn = "";
	private String allowDateStS = "";
	private String allowDateEndS = "";
	private Date allowDateSt = null;
	private Date allowDateEnd = null;

	public int getSeq() {
		return seq;
	}

	public void setSeq(int seq) {
		this.seq = seq;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getUseYn() {
		return useYn;
	}

	public void setUseYn(String useYn) {
		this.useYn = useYn;
	}

	public Date getRegDt() {
		return regDt;
	}

	public void setRegDt(Date regDt) {
		this.regDt = regDt;
	}

	public Date getModDt() {
		return modDt;
	}

	public void setModDt(Date modDt) {
		this.modDt = modDt;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getIpBandA() {
		return ipBandA;
	}

	public void setIpBandA(String ipBandA) {
		this.ipBandA = ipBandA;
	}

	public String getIpBandB() {
		return ipBandB;
	}

	public void setIpBandB(String ipBandB) {
		this.ipBandB = ipBandB;
	}

	public String getAllowDateUseYn() {
		return allowDateUseYn;
	}

	public void setAllowDateUseYn(String allowDateUseYn) {
		this.allowDateUseYn = allowDateUseYn;
	}

	public String getAllowDateStS() {
		return allowDateStS;
	}

	public void setAllowDateStS(String allowDateStS) {
		this.allowDateStS = allowDateStS;
	}

	public String getAllowDateEndS() {
		return allowDateEndS;
	}

	public void setAllowDateEndS(String allowDateEndS) {
		this.allowDateEndS = allowDateEndS;
	}

	public Date getAllowDateSt() {
		return allowDateSt;
	}

	public void setAllowDateSt(Date allowDateSt) {
		this.allowDateSt = allowDateSt;
	}

	public Date getAllowDateEnd() {
		return allowDateEnd;
	}

	public void setAllowDateEnd(Date allowDateEnd) {
		this.allowDateEnd = allowDateEnd;
	}
}
